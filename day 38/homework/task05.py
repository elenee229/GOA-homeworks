#6) შექმენით tuple სახელად animals, რომელშიც იქნება რამდენიმე ცხოველის სახელი. გამოიყენეთ .count და .index მეთოდები ამ tuple-ზე
animals = ('dogs','rabbits','cows','pigs','rabbits')

print(animals.count('rabbits'))
print(animals.count('pigs'))
print(animals.index('dogs'))
print(animals.index('cows'))